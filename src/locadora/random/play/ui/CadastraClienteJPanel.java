    private void carregarTabela(){
        DefaultTableModel dfm = (DefaultTableModel) clientesJTable.getModel();
        dfm.setRowCount(0);
        
        List<Cliente> lista = banco.consultarLimitado(20);
        for (Cliente cliente : lista) {
            Object[] linha = new Object[6];
            linha[0] = cliente.getId();
            linha[1] = cliente.getNome();
            linha[2] = cliente.getCpf();
            linha[3] = cliente.getDataNasc();
            linha[4] = cliente.getEmail();
            linha[5] = cliente.getEndereco();
            dfm.addRow(linha);
        }
    }
    private void resultaBuscaNome(String nome){
        DefaultTableModel dfm = (DefaultTableModel) clientesJTable.getModel();
        dfm.setRowCount(0);
        
        List<Cliente> lista = banco.buscarNome(nome);
        for (Cliente cliente : lista) {
            Object[] linha = new Object[6];
            linha[0] = cliente.getId();
            linha[1] = cliente.getNome();
            linha[2] = cliente.getCpf();
            linha[3] = cliente.getDataNasc();
            linha[4] = cliente.getEmail();
            linha[5] = cliente.getEndereco();
            dfm.addRow(linha);
        }
    }
