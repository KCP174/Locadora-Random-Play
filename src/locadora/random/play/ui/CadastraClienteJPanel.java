    private void excluirJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirJButtonActionPerformed
        int[] linhas = clientesJTable.getSelectedRows();
        if (linhas.length > 1){
            textoExclJLabel.setText("Excluir " + linhas.length + " registros?");
            confirmaExclJDialog.setVisible(true);
        }  else if (linhas.length == 1){
            textoExclJLabel.setText("Excluir " + clientesJTable.getValueAt(linhas[0], 1) + ", CPF: " + clientesJTable.getValueAt(linhas[0], 2) + "?");
            confirmaExclJDialog.setVisible(true);
        } else{
            JOptionPane.showMessageDialog(excluirJButton, "Escolha um registro para excluir!");
        }
    }//GEN-LAST:event_excluirJButtonActionPerformed

