    private void buscarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarJButtonActionPerformed
        if (!cpfJFormattedTextField.getText().equals("   .   .   -  ")){
            carregarTabelaLocacoesCliente();
            if(cliente == null){
                JOptionPane.showMessageDialog(buscarJButton, "CPF não encontrado no sistema!");
            } else if (locacoesJTable.getRowCount() == 0){
                JOptionPane.showMessageDialog(buscarJButton, "CPF fornecido não tem locações ativas!");
            }
        }else{
            JOptionPane.showMessageDialog(buscarJButton, "Insira um CPF válido!");
        }
    }//GEN-LAST:event_buscarJButtonActionPerformed

    private void selecionarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarJButtonActionPerformed
        if (locacoesJTable.getSelectedRowCount() == 1){
            calculaMulta();
            carregarTabelaItens((int) locacoesJTable.getValueAt(locacoesJTable.getSelectedRow(), 0));
            selecionadaJTextField.setText(locacoesJTable.getValueAt(locacoesJTable.getSelectedRow(), 0).toString());
        }else{
            JOptionPane.showMessageDialog(selecionarJButton, "Selecione 1 Locação!");
        }
    }//GEN-LAST:event_selecionarJButtonActionPerformed

    private void devolverJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_devolverJButtonActionPerformed
        if(!selecionadaJTextField.getText().isBlank()){
            if(!multaJTextField.getText().equals("R$ 0.00")){
                JOptionPane.showMessageDialog(devolverJButton, "A pagar: " + multaJTextField.getText() + " de multa devido atraso!");
            }
            bancoLocacao.devolver(Integer.parseInt(selecionadaJTextField.getText()));
            JOptionPane.showMessageDialog(devolverJButton, "Locação devolvida com sucesso!");
            limparJButtonActionPerformed(evt);
        }else{
            JOptionPane.showMessageDialog(devolverJButton, "Selecione 1 Locação!");
        }
    }//GEN-LAST:event_devolverJButtonActionPerformed

