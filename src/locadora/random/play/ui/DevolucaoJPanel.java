    private void carregarTabelaLocacoesCliente(){
        DefaultTableModel dfm = (DefaultTableModel) locacoesJTable.getModel();
        dfm.setRowCount(0);
        cliente = bancoCliente.buscarPorCpf(cpfJFormattedTextField.getText());
        List<Locacao> lista = new ArrayList<>();
        if (cliente != null){
            lista = bancoLocacao.consultarLocacoesAtivasDoCliente(cliente.getId());
            for (Locacao locacao : lista) {
            
                Object[] linha = new Object[4];
                linha[0] = locacao.getId();
                linha[1] = locacao.getDataLocacao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"));
                linha[2] = locacao.getDataDevolucao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy"));
                linha[3] = locacao.getValorTotal();
                dfm.addRow(linha);
            }
        }   
    }
    
    private void carregarTabelaItens(int id){
        DefaultTableModel dfm = (DefaultTableModel) itensJTable.getModel();
        dfm.setRowCount(0);
        
        List<ItemLocacao> lista = bancoLocacao.consultarItens(id);
        for (ItemLocacao item : lista) {
            
            Object[] linha = new Object[3];
            linha[0] = item.getId();
            linha[1] = bancoLocacao.retornaTitulo(item.getIdFilme());
            linha[2] = item.getValorLocacao();
            dfm.addRow(linha);
        } 
    }
    
    private void calculaMulta(){
        int selecionada = locacoesJTable.getSelectedRow();
        LocalDate dataPrevista = LocalDate.parse((String) locacoesJTable.getValueAt(selecionada, 2), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        LocalDate dataLocacao = LocalDate.parse((String) locacoesJTable.getValueAt(selecionada, 1), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        int diasAtrasados = (int) ChronoUnit.DAYS.between(dataPrevista, LocalDate.now());
        int diasLocados = (int) ChronoUnit.DAYS.between(dataLocacao, dataPrevista);
        
        
        if (diasAtrasados >= 0){
            Double valorTotal = (Double) locacoesJTable.getValueAt(selecionada, 3);
            Double valorDiario = valorTotal / diasLocados;         
            Double multa = diasAtrasados * valorDiario;
            BigDecimal bd = new BigDecimal(multa);
            bd = bd.setScale(2, RoundingMode.HALF_UP);
            multa = bd.doubleValue();
            multaJTextField.setText("R$ " + multa.toString());
        } else{
            multaJTextField.setText("R$ 0.00");
        }
    }
    

