    private void cadastrarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarJButtonActionPerformed
        try {
        Funcionario novoFuncionario = new Funcionario(nomeJTextField.getText(), loginJTextField.getText(), senhaJTextField.getText(), dataNascJFormattedTextField.getText(), cpfJFormattedTextField.getText());
        String idStr = idJTextField.getText();  
            try {
                int id = Integer.parseInt(idStr);
                novoFuncionario.setId(id);
                banco.atualizar(novoFuncionario);
                JOptionPane.showMessageDialog(cadastrarJButton, "Funcionario atualizado!");
                cancelarJButtonActionPerformed(evt);
            } catch(NumberFormatException erro){
            //Verificar se o cpf já está no sistema
                List<Funcionario> listaFuncionario = banco.consultar();
                String cpf0 = novoFuncionario.getCpf();
                boolean cadastrado = false;
                for (Funcionario c : listaFuncionario){
                if (c.getCpf().equals(cpf0)){
                    JOptionPane.showMessageDialog(cadastrarJButton, "CPF já registrado!");
                    cadastrado = true;
                    break;
                }
            }        
            //Fim verificaçõa
            if (!cadastrado){
                banco.inserir(novoFuncionario);
                JOptionPane.showMessageDialog(cadastrarJButton, "Funcionario cadastrado com sucesso!");
                cancelarJButtonActionPerformed(evt);
            }}
        
            } catch(Exception erro) {
            erro.printStackTrace();
            JOptionPane.showMessageDialog(cadastrarJButton, erro.getMessage());
        }
        carregarTabela();
    }//GEN-LAST:event_cadastrarJButtonActionPerformed

