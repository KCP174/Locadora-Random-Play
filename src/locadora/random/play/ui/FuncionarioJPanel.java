    private void carregarTabela(){
        DefaultTableModel dfm = (DefaultTableModel) funcionariosJTable.getModel();
        dfm.setRowCount(0);
        
        List<Funcionario> lista = banco.consultar();
        lista.remove(0);
        for (Funcionario funcionario : lista) {
            Object[] linha = new Object[6];
            linha[0] = funcionario.getId();
            linha[1] = funcionario.getNome();
            linha[2] = funcionario.getCpf();
            linha[3] = funcionario.getDataNasc();
            linha[4] = funcionario.getLogin();
            linha[5] = funcionario.getSenha();
            dfm.addRow(linha);
        }
    }
    private void resultaBuscaNome(String nome){
        DefaultTableModel dfm = (DefaultTableModel) funcionariosJTable.getModel();
        dfm.setRowCount(0);
        
        List<Funcionario> lista = banco.buscar(nome);
        if (lista.get(0).getNome().equals("admin")){
            lista.remove(0);
        }
        for (Funcionario funcionario : lista) {
            Object[] linha = new Object[6];
            linha[0] = funcionario.getId();
            linha[1] = funcionario.getNome();
            linha[2] = funcionario.getCpf();
            linha[3] = funcionario.getDataNasc();
            linha[4] = funcionario.getLogin();
            linha[5] = funcionario.getSenha();
            dfm.addRow(linha);
        }
    }
