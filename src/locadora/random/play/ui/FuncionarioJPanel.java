    private void excluirJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirJButtonActionPerformed
        int[] linhas = funcionariosJTable.getSelectedRows();
        if (linhas.length > 1){
            textoExclJLabel.setText("Excluir " + linhas.length + " registros?");
            confirmaExclJDialog.setVisible(true);
        }  else if (linhas.length == 1){
            textoExclJLabel.setText("Excluir " + funcionariosJTable.getValueAt(linhas[0], 1) + ", CPF: " + funcionariosJTable.getValueAt(linhas[0], 2) + "?");
            confirmaExclJDialog.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(excluirJButton, "Escolha um registro para excluir!");
        }
    }//GEN-LAST:event_excluirJButtonActionPerformed

    private void simJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simJButtonActionPerformed
        int[] linhas = funcionariosJTable.getSelectedRows();
        for (int selecionado : linhas){
            Object id = funcionariosJTable.getValueAt(selecionado, 0);
            Funcionario apagado = new Funcionario();
            apagado.setId((int)id);
            banco.remover(apagado);
        }
        confirmaExclJDialog.setVisible(false);
        carregarTabela();
    }//GEN-LAST:event_simJButtonActionPerformed

